processData.InertialSignals<-function(directory,type){
  
# Get the 128-vector files of the sliding window
inertial.signals.files<-dir(paste(directory,type,"Inertial Signals",sep="\\"))

preprocessInertialSignalsFile<-function(path,fileName){
  # Generate the column names
  prefix<-"SW"
  suffix<-seq(1:128)
  data.inertial.colnames<-paste(gsub("[train]|[.txt]|[test]","",fileName),prefix,suffix,sep="_")
  
  # Read the data frame
  df<-read.table(paste(path,fileName,sep="\\"),header=FALSE)
  
  # set the column names
  colnames(df)<-data.inertial.colnames
  
  # Return dataframe
  df
}

# Load the inertial signals data, each data file becomes a list element
inertial.signals<-lapply(inertial.signals.files,function(x) preprocessInertialSignalsFile(paste(directory,type,"Inertial Signals",sep="\\"),x))
# Column bind of all list elements
inertial.signals<-do.call(cbind,inertial.signals) # build a combined data frame

inertial.signals

}

